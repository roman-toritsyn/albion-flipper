const BY_LOCALE = Object.fromEntries(
  LOCALE_OPTIONS.map((o) => [o.locale, o]),
) as Record<AppLocale, LocaleOption>;

export function isAppLocale(v: string): v is AppLocale {
  return v in BY_LOCALE;
}

export function localeOption(locale: AppLocale): LocaleOption {
  return BY_LOCALE[locale] ?? BY_LOCALE[DEFAULT_LOCALE];
}

export function itemLangFor(locale: AppLocale): DumpLang {
  return localeOption(locale).itemLang;
}

